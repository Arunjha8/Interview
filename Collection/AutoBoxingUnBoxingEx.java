package Collection;

public class AutoBoxingUnBoxingEx {
    public static void main(String[] args) {
        //AutoBoxing
        int i = 10;
        Integer i1 = i;
        System.out.println(i1);

        //Unboxing//
        Integer a = new Integer(20);
        int a2 = a;
        System.out.println(a2);

    }
}
